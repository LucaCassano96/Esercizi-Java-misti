public class Specie {

    //VARIABILI DI ISTANZA
    private String nomeSpecie;
    private int popolazioneCorrente;
    private int tassoDiCrescita;
    private int anniCrescita;


    //GETTER E SETTER
    public String getNomeSpecie() {
        return nomeSpecie;
    }

    public void setNomeSpecie(String nomeSpecie) {
        this.nomeSpecie = nomeSpecie;
    }

    public int getPopolazioneCorrente() {
        return popolazioneCorrente;
    }

    public void setPopolazioneCorrente(int popolazioneCorrente) {
        this.popolazioneCorrente = popolazioneCorrente;
    }

    public int getTassoDiCrescita() {
        return tassoDiCrescita;
    }

    public void setTassoDiCrescita(int tassoDiCrescita) {
        this.tassoDiCrescita = tassoDiCrescita;
    }

    public int getAnniCrescita() {
        return anniCrescita;
    }

    public void setAnniCrescita(int anniCrescita) {
        this.anniCrescita = anniCrescita;
    }


}
